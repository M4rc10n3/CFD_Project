function Euler_1D(pb_cont,pb_stop,fig_rho,fig_pre,fig_vel,fig_tem,bg_isch,pop_test,edit_NC,edit_CFL,edit_IORD,edit_rhoL,edit_uL,edit_pL,edit_Gamma,edit_rhoR,edit_uR,edit_pR,edit_x0,edit_timemax,edit_ka,txt_stepcounter2,txt_time2) %(1D and quasi-1D Euler equations)

%C       (perfect gas (gamma=1.4), primitive variables P,U,H)

global gamma ga gb gc gd ge gf gg gh gi gj % era USE PIGAMMA_PAR
global nc ncm k kout ka iord itest stab  % era USE NUM_PARs
global b c diverg c1 c2 x0 dx            %        era USE GEOM_PAR
global x                                          % era USE GEOM_VAR
global   time dt dtodx dtitest4 timemax timek    % era USE TIME_PAR
global p t u s rho a e amach ptot ttot flow flht h htot % era USE VARS
global w1 w2 w3 f1 f2 f3 phi1 phi2 phi3                    % era USE VARS
global p t u s rho a e amach ptot ttot flow flht h htot % era USE VARS
global w1 w2 w3 f1 f2 f3 phi1 phi2 phi3                    % era USE VARS
%        USE BC
global nrt rtrms rtmax                    % era USE CONVERG
global  xsh10 xsh20 p1 rho1 u1 p2 rho2 u2 p3 rho3 u3 p4 rho4 u4 % era USE INIT_PAR
global  vsh1 vsh2 vsh3 vsh4 vsh5 vsh3l vsh3r vsh4l vsh4r % era USE INIT_PAR
global  xsh1 xsh2 xsh3 xsh4 xsh5 xsh3l xsh3r xsh4l xsh4r % era USE INIT_PAR
global pth tth uth sth rhoth ath eth amachth  ttotth flowth flhtth hth % era USE THEORETICAL
global htotth r1th r2th r3th fplotth xth % era USE THEORETICAL
global xshth fthshu fthshd pu pd rhou tu su sd uu ud amu amd % era USE THEORETICAL
global htotu htotd flowu flowd flhtu flhtd r1u r1d r2u r2d r3u r3d % era USE THEORETICAL
global x1 x2 x3 x4 x5 % era USE THEORETICAL
global p1l p1r u1l u1r s1l s1r a1l a1r t1l t1r rho1l rho1r e1l e1r % era USE THEORETICAL
global p2l p2r u2l u2r s2l s2r a2l a2r t2l t2r rho2l rho2r e2l e2r % era USE THEORETICAL
global p3l p3r u3l u3r s3l s3r a3l a3r t3l t3r rho3l rho3r e3l e3r % era USE THEORETICAL
global p4l p4r u4l u4r s4l s4r a4l a4r t4l t4r rho4l rho4r e4l e4r % era USE THEORETICAL

if (pb_cont.Value == 0)

% Le stringhe date come input vengono trasformate in numeri
nc = str2double(edit_NC.String);
iord = str2double(edit_IORD.String);
stab = str2double(edit_CFL.String);

% Questa è la variabile che contiene il numero che identifica lo schema 
% di calcolo:
ischeme = bg_isch.Value; % 1 -> PFD-Osher, 2 -> AUSM+, 3 -> AUSMPW

gamma = str2double(edit_Gamma.String);
rhol = str2double(edit_rhoL.String);
ul   = str2double(edit_uL.String);
pl   = str2double(edit_pL.String); 
rhor = str2double(edit_rhoR.String);
ur   = str2double(edit_uR.String);
pr   = str2double(edit_pR.String);

% Posizione in cui vado a inserire la discontinuità
x0   = str2double(edit_x0.String);

fprintf('Euler_1D (FDS - 1st and 2nd Order) \n')
k=1; % Numero del passo
time=0.0;
timek(k)=time; %Tempo al passo k

if (pop_test.Value)==1
    itest = 2;
    ka = str2double(edit_ka.String);  % ka è il numero totale di passi temporali
    timemax = str2double(edit_timemax.String); % timemax è il tempo massimo di integrazione
elseif (pop_test.Value)==2
    itest = 1;
elseif (pop_test.Value)>=3
    itest = pop_test.Value;
    ka = str2double(edit_ka.String);
    timemax = str2double(edit_timemax.String);    
end

% Distribuisce le condizioni iniziali all'interno della griglia di calcolo
init(rhol,ul,pl,rhor,ur,pr);

kk=sprintf('%.0f',ka);
edit_ka.String = kk;     

% Routine che risolve il problema di Riemann in maniera esatta: si trovano
% i valori delle variabili nelle 4 zone "a, b, c e d"; "alamx" è la
% "lambda" della superficie di contatto.
[utha,uthb,uthc,uthd,ptha,pthb,pthc,pthd,atha,athb,athc,athd,rhotha,rhothb,rhothc,rhothd,stha,sthb,sthc,sthd,alama,alamb,alamc,alamd,alamx,vshl,vshr,el,er] = rpsolver();

kk=sprintf('%.0f',k);
txt_stepcounter2.String = kk;    

tt=sprintf('%.6f',time);
%idx=regexp(tt,'0+','start');
%tt(idx(end):end)=[];
txt_time2.String = tt;    

% *** plot the initial conditions
figure(fig_rho)
plot(x,rho,'o-')
figure(fig_pre)
plot(x,p,'o-')
figure(fig_vel)
plot(x,u,'o-')
figure(fig_tem)
plot(x,t,'o-')
end

% Si esegue se si schiaccia su "Continue"
if (pb_cont.Value == 1)
    ka = str2double(edit_ka.String);
    if (pop_test.Value == 2)
        timemax = dt*(ka-1);
        tt=sprintf('%.6f',timemax);
        edit_timemax.String = tt;
    else
        timemax= str2double(edit_timemax.String);
        stab= str2double(edit_CFL.String);

    end
    [utha,uthb,uthc,uthd,ptha,pthb,pthc,pthd,atha,athb,athc,athd,rhotha,rhothb,rhothc,rhothd,stha,sthb,sthc,sthd,alama,alamb,alamc,alamd,alamx,vshl,vshr,el,er] = rpsolver();
end

while (k < ka) && (time < timemax)
    k=k+1;
    fprintf('K =%i - TIME=%f \n',k,time)
    
    dt=1.e5;
    % Al denominatore c'è il valore massimo delle "lambda"
    dtd(2:ncm) = dx./(abs(u(2:ncm))+a(2:ncm));
    dt = min(dtd(2:ncm));
    
    if itest == 1
        dt = dtodx * dx;
    elseif itest >=2
        dt=dt*stab;
    end
    time = time+dt;
    timek(k) = time;

kk=sprintf('%.0f',k);
txt_stepcounter2.String = kk;    

tt=sprintf('%.6f',time);
txt_time2.String = tt;    

    if (iord == 2)  % se si è scelto di fare 2nd order, 
        % allora calcolo pendenze e derivate temporali in eno1
        eno1()
    end

%% Scelta dello schema 

    %Osher di D'Ambrosio
    if ischeme == 1
        split()
        march()
    end

    %AUSM+ implementato da noi
    if ischeme == 2
        split_ausm_plus2()
        march()
    end

    %AUSM+ che dovrebbe essere corretto
    if ischeme == 3
        split_1()
        march()
    end
    
    if (itest == 1)
        sum1=mean(w1);
        sum2=mean(w2) + (p(nc)-p(1))*time;
        sum3=mean(w3);
        
        fprintf('K=%i - sum1=%f - sum2=%f - sum3=%f \n',k,sum1,sum2,sum3);
    end

% Si creano i grafici
figure(fig_rho)
hold off
plot(x,rho,'o-')
hold on

figure(fig_pre)
hold off
plot(x,p,'o-')
hold on

figure(fig_vel)
hold off
plot(x,u,'o-')
hold on

figure(fig_tem)
hold off
plot(x,t,'o-')
hold on

%      CALL GNUPLOT
%       costruire routine per output dati e visualizzazione

% Si valuta anche la soluzione teorica
thesol(timek(k),utha,uthb,uthc,uthd,ptha,pthb,pthc,pthd,atha,athb,athc,athd,rhotha,rhothb,rhothc,rhothd,stha,sthb,sthc,sthd,alama,alamb,alamc,alamd,alamx,vshl,vshr,el,er);
figure(fig_rho)
plot(xth,rhoth,'o-')

figure(fig_pre)
plot(xth,pth,'o-')

figure(fig_vel)
plot(xth,uth,'o-')

figure(fig_tem)
plot(xth,pth./rhoth,'o-')
end

pb_cont.Enable = 'On';
pb_stop.Enable = 'On';
edit_ka.String = '(increase or stop)';

%if (pb_stop.Value) == 1
%    return
%end

end